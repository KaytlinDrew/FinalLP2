    private void establecerJerarquiaDepartment(DefaultMutableTreeNode raiz, ArrayList<Department> departments) throws RemoteException{
        for(Department d : departments){
            DefaultMutableTreeNode node = new DefaultMutableTreeNode(d);
                        
            ArrayList<Employee> employees = new ArrayList<Employee>();
            ArrayList<Department> subDepartments = new ArrayList<Department>();
            
                    
            
            employees = daoEmployee.queryAllIndependent(d.getId());
            establecerJerarquiaEmployee(node, employees);
            
            raiz.add(node);
            
            subDepartments = daoDepartment.queryAllDependent(d.getId());
            establecerJerarquiaDepartment( node, subDepartments );
                   
            
                
        }
    }
    
    private void establecerJerarquiaEmployee(DefaultMutableTreeNode raiz, ArrayList<Employee> employees) throws RemoteException{
        for(Employee e: employees){
            DefaultMutableTreeNode node = new DefaultMutableTreeNode(e);
                  
            ArrayList<Employee> subemployees = new ArrayList<Employee>();
                        
                     
            subemployees = daoEmployee.queryAllDependent(e.getId());
            establecerJerarquiaEmployee( node,  subemployees );
            raiz.add(node);
               
        }
    }
    
    
