    private void clickEmployee(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_clickEmployee
        // TODO add your handling code here:
        
        if (evt.getClickCount() ==2){
            
            DefaultMutableTreeNode nodo = (DefaultMutableTreeNode) treeDepartments.getLastSelectedPathComponent();
            
                Employee e = (Employee) nodo.getUserObject();
                
                dni.setText(e.getDNI());
                
                name.setText(e.getName() +" "+ e.getLastName() +" "+ e.getSecondLastName());
                role.setText(e.getRole().getName());
                
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                
                birthdate.setText(sdf.format(e.getBirthday()));
                email.setText(e.getEmail());
                salary.setText( Double.toString(e.getSalary()));
                 
        }
        
        
    }//GEN-LAST:event_clickEmployee

